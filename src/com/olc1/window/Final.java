    private void btnAnalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalizarActionPerformed
        // TODO add your handling code here:
        //Limpia el txtbox donde se muestran los resultado
        this.txtOutput.setText("");
        Scanner scan = new Scanner(new BufferedReader(new StringReader(this.txtInput.getText())));
        Parser parser = new Parser(scan);
        try{
            parser.parse();
            //Ambas cadenas sirven para concatenar el contenido de la lista de nodos
            String concat = "";
            String result = "";
            //Recorre la lista de listas
            for(LinkedList<Nodo> nodo : parser.resultado){
                //Recorre la lista
                for (Nodo nodito : nodo){
                    //Concatena las cadenas
                    concat += nodito.getConcatenado() + ", ";
                    result += nodito.getResultado() + ", ";
                }
                //Muestra en el txtbox el resultado del an√°lisis
                this.txtOutput.setText(this.txtOutput.getText() + "Cadena 1:\n"+ concat.substring(0, concat.length()-2) + "\nCadena 2:\n" + result.substring(0, result.length()-2) + "\n");
                //Limpia las cadenas 
                concat = "";
                result = "";
            }
        } catch (Exception ex){
            JOptionPane.showMessageDialog(this, "Ocurrio un error (" + ex.getMessage() + ")", "[OLC1] - FINAL", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAnalizarActionPerformed

